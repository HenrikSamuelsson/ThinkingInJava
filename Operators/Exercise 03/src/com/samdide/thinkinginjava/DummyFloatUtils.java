package com.samdide.thinkinginjava;

/**
 * Dummy class with method to alter a DummyFloat object.
 * 
 * @author Henrik Samuelsson
 */
public class DummyFloatUtils {
	/**
	 * Will set the f field of the DummyFloat argument to zero.
	 * 
	 * @param df
	 *            the object that shall be reset
	 */
	static void reset(DummyFloat df) {
		df.f = 0f;
	}
}
